int main() {
    cin.tie(0);ios::sync_with_stdio(false);
    int64 N,Q; cin >> N >> Q;
    vector<int64> B(Q); 
    priority_queue_reverse<int64> pqA,pqB;
    for(int i=0;i<N;++i) {
        int64 a; cin >> a; 
        pqA.push(a);
    }
    for(int i=0;i<Q;++i) {
        cin >> B[i];
        pqB.push(B[i]);
    }
    int64 ofs = 0;
    map<int64,int64> mp;
    while(pqA.size() || pqB.size()) {
        if(pqB.empty()) {
            break;
        }
        if(pqA.empty()) {
            int64 b = pqB.top(); pqB.pop();
            mp[b] = ofs;
            continue;
        }
        int64 a = pqA.top(); pqA.pop();
        int64 b = pqB.top(); pqB.pop();
        if(a <= b + ofs) {
            ofs += 1;
            pqB.push(b);
        }
        else {
            mp[b] = ofs;
            pqA.push(a);
        }
    }
    for(auto b:B) {
        cout << b + mp[b] << "\n";
    }
    return 0;
}
