int main() {
    cin.tie(0);ios::sync_with_stdio(false);
    int N,M; cin >> N >> M;
    vector<int> d(N,0);
    vector<vector<int>> edge(N);
    for(int i=0;i<M;++i) {
        int s,t; cin >> s >> t;
        s--,t--;
        if(s==-1) {
            d[t] = 1;
        }
        else {
            edge[s].push_back(t);
            edge[t].push_back(s);
        }
    }
    vector<int64> dp0(N,LOWINF);
    vector<int64> dp1(N,LOWINF);
    queue<int> q;
    q.push(0);
    dp0[0] = 0;
    while(q.size()) {
        int from = q.front(); q.pop();
        for(int to:edge[from]) {
            if(dp0[to] == LOWINF) {
                dp0[to] = dp0[from] + 1;
                q.push(to);
            }
        }
    }
    q.push(N-1);
    dp1[N-1] = 0;
    while(q.size()) {
        int from = q.front(); q.pop();
        for(int to:edge[from]) {
            if(dp1[to] == LOWINF) {
                dp1[to] = dp1[from] + 1;
                q.push(to);
            }
        }
    }
    int64 q0 = LOWINF, q1 = LOWINF;
    for(int i=0;i<N;++i) {
        if(d[i]) chmin(q0, dp0[i]);
        if(d[i]) chmin(q1, dp1[i]);
    }
    for(int i=0;i<N;++i) {
        int64 ans = LOWINF;
        chmin(ans,dp0[N-1]);
        chmin(ans,dp1[0]);
        chmin(ans,dp0[i]+dp1[i]);
        chmin(ans,q0+q1+2);
        chmin(ans,q0+dp1[i]+1);
        chmin(ans,dp0[i]+q1+1);
        if(ans == LOWINF) ans = -1;
        cout << ans << " \n"[i==N-1];
    }
    
    return 0;
}
