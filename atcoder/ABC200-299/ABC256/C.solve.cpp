int main() {
    cin.tie(0);ios::sync_with_stdio(false);
    vector<int> H(3),W(3);
    for(int i=0;i<3;++i) cin >> H[i];
    for(int i=0;i<3;++i) cin >> W[i];
    int M = 30;

    int64 ans = 0;
    for(int a00=1;a00<=M;++a00) {
        for(int a01=1;a01<=M;++a01) {
            for(int a10=1;a10<=M;++a10) {
                for(int a11=1;a11<=M;++a11) {
                    int a02 = H[0] - (a00 + a01);
                    if(a02 <= 0) continue;
                    int a12 = H[1] - (a10 + a11);
                    if(a12 <= 0) continue;
                    int a20 = W[0] - (a00 + a10);
                    if(a20 <= 0) continue;
                    int a21 = W[1] - (a01 + a11);
                    if(a21 <= 0) continue;
                    int a22 = H[2] - (a20 + a21);
                    if(a22 <= 0) continue;

                    if(W[2] != a02 + a12 + a22) continue;
                    ans++;

                }
            }
        }
    }
    cout << ans << endl;
    return 0;
}
